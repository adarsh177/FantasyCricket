function ProcessMatchesAndTeams(){
    const files = fs.readdirSync('./JSON\ Files');

    const data = {};
    const teams = {};
    let count = 0;

    files.forEach(fl => {

        if(fl.includes("json")){
            // is a valid file
            var fContent = JSON.parse(fs.readFileSync(`./JSON\ Files/${fl}`, "utf-8"));
            data[`m${count}`] = {
                t1: fContent.info.teams[0],
                t2: fContent.info.teams[1],
                id: fl.split('.')[0]
            }

            // adding team
            if(teams[fContent.info.teams[0]] !== true){
                teams[fContent.info.teams[0]] = true;
            }
        }
        
        count++;
    });

    fs.writeFileSync(`./output/MatchData.json`, JSON.stringify({
        count: count,
        matches: data
    }, null, 2));

    fs.writeFileSync(`./output/Teams.json`, JSON.stringify(teams, null, 2));

    console.log("Count", count);
    console.log("teams", teams);

}